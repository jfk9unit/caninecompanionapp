// Share button component for individual platforms
export const SocialShareButton = ({ platform, url, text, variant = "default", size = "default" }) => {
  const social = SOCIAL_PLATFORMS[platform];
  if (!social) return null;
  
  const handleShare = () => {
    const shareUrl = social.getShareUrl(url, text);
    window.open(shareUrl, '_blank', 'width=600,height=400');
    toast.success(`Opening ${social.name}...`);
  };
  
  if (variant === "icon") {
    return (
      <Button
        onClick={handleShare}
        size="icon"
        className="rounded-full transition-all hover:scale-110"
        style={{ backgroundColor: social.color }}
        data-testid={`share-${platform}-btn`}
      >
        {social.icon}
      </Button>
    );
  }
  
  return (
    <Button
      onClick={handleShare}
      className="w-full justify-start gap-3 text-white transition-all hover:opacity-90"
      style={{ backgroundColor: social.color }}
      data-testid={`share-${platform}-btn`}
    >
      {social.icon}
      Share on {social.name}
    </Button>
  );
};

// Social share bar component
export const SocialShareBar = ({ url, text, title = "Share" }) => {
  const [copied, setCopied] = useState(false);
  
  const copyToClipboard = async () => {
    try {
      await navigator.clipboard.writeText(url);
      setCopied(true);
      toast.success("Link copied to clipboard!");
      setTimeout(() => setCopied(false), 2000);
    } catch (err) {
      toast.error("Failed to copy link");
    }
  };
  
  return (
    <div className="flex items-center gap-2">
      <span className="text-sm text-gray-500">{title}:</span>
      <div className="flex gap-2">
        {Object.keys(SOCIAL_PLATFORMS).map((platform) => (
          <SocialShareButton
            key={platform}
            platform={platform}
            url={url}
            text={text}
            variant="icon"
          />
        ))}
        <Button
          onClick={copyToClipboard}
          size="icon"
          variant="outline"
          className="rounded-full"
          data-testid="copy-link-btn"
        >
          {copied ? <Check className="w-4 h-4 text-green-500" /> : <Copy className="w-4 h-4" />}
        </Button>
      </div>
    </div>
  );
};

